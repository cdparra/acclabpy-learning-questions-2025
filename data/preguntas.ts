// Actualizar la función para obtener preguntas por ciclo
export function getPreguntasPorCiclo(ciclo: string): Pregunta[] {
  // Mapeo inverso (nuevos nombres a antiguos)
  const mapeoInverso: Record<string, string[]> = {
    "Digitalización de MIPYMES": ["Local Digital"],
    "Gobernanza Participativa": ["Tavarandú", "Gobernanza Participativa"],
    "Empleabilidad y Juventud": ["Empleo Justo", "Empleabilidad y Juventud"],
    "Capacidades de Innovación Pública": ["Innovadores Públicos"],
    "Productividad y Trabajo Decente en MIPYMES": ["Local"],
    "Gestión Pública Innovadora": ["Gestión Pública Innovadora"],
    "Ciudades y Movilidad": ["Ciudades y Movilidad"],
    "Energías Renovables": ["Energías Renovables"],
  }

  // Si el ciclo es uno de los nuevos nombres, buscar por los nombres originales
  const ciclosOriginales = mapeoInverso[ciclo] || [ciclo]

  return data.filter((item) => ciclosOriginales.includes(item.cicloAprendizaje))
}

